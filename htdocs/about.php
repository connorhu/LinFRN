RaspRN is an FRN gateway client for the Raspberry Pi.  The project aims to provide a solution for FRN gateway owners looking for a cheap, low-power, dedicated FRN client to connect to their gateway radio.  Hardware for this project costs around &pound;45 in total, including the Pi, and the software is open source, released under the GPL.
</p>
